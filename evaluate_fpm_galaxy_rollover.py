def hash_file(filepath):
    """Generate SHA-256 hash of the input structural data file to guarantee lock."""
    h = hashlib.sha256()
    with open(filepath, 'rb') as file:
        while chunk := file.read(8192):
            h.update(chunk)
    return h.hexdigest()

